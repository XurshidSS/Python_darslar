# any() funksiyasi
numbers = (1, 2, 3, 4, 5, 6)
print(any(numbers))

# max() fuksiyasi
numbers = (1, 2, 3, 4, 5, 6)
print(max(numbers))

# min() finksiyasi
numbers = (1, 2, 3, 4, 5, 6)
print(min(numbers))

# len() funksiyasi
numbers = (1, 2, 3, 4, 5, 6)
print(len(numbers))

# sorted() funksiyasi
numbers = (10, 3, 6, 4, 5, 2)
print(sorted(numbers))

# sum() funksiyasi
numbers = (1, 2, 3, 4, 5, 6)
print(sum(numbers))